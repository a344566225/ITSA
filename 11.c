int main()
{
    int n, t, m, k;
    scanf("%d", &n);
    for (int i = 0; i < n; i++)
    {
        scanf("%d %d %d", &t, &m, &k);
        int price[k];
        for (int j = 0; j < k; j++)
            scanf("%d", &price[j]);
    }
}